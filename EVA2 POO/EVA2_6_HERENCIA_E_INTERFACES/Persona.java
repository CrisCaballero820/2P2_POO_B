public class Persona {
    private String Nombre;
    private String Edad;

    public Persona() {
        this.Nombre = Nombre;
        this.Edad = Edad;
    }

    public Persona(String Nombre, String Edad) {
        this.Nombre = Nombre;
        this.Edad = Edad;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getEdad() {
        return Edad;
    }

    public void setEdad(String Edad) {
        this.Edad = Edad;
    }

    
    
}